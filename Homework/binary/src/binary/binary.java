package binary;

public class binary {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println("Suma: a + b = " + (a + b)+ " binarinė israiska:" + Integer.toBinaryString(a+b));
		System.out.println("Skirtumas: a - b = " + (a - b)+ " binarinė israiska:" + Integer.toBinaryString(a-b));
		System.out.println("Daugyba: a * b = " + (a * b)+ " binarinė israiska:" + Integer.toBinaryString(a*b));
		System.out.println("Dalyba: b / a = " + (b / a)+" binarinė israiska:" + Integer.toBinaryString(b/a));

	}

}
